package ex02;

public class Ex3 {
	
	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 20;
		
		System.out.println(num1 == num2);   //같다
		 
		System.out.println(num1 != num2);   //같지 않다
		
		System.out.println(num1 > num2);       // num1 이 num2 보다 크다
		
		System.out.println(num1 >= num2);      //num1 이 num2 보다 크거나 같다
		
		System.out.println(num1 < num2);        // num1 이 num2 보다 작다
		
		System.out.println(num1 <= num2);        //num1 이 num2 보다 작거나 같다
		
		
	}

}
